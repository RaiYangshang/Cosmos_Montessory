             <h1> Our Programs</h1>
             <p> Cosmos invites students age 2-6 to join our warm and welcoming school community. 
			     Our Pre-Primary classroom stresses the development of independence, self-reliance, social skills
				 and a love of learning. Primary students build the fundamentals of math and reading and hone 
				 practical life skills in addition to exploring science, history, and art. Our kindergarteners 
				 pursue advanced lessons in preparation for a successful transition to first grade.
			 </p>	
             <h2>Pre-Primary
                 Ages 2-3
			 </h2>
			 <p>We work hard to ensure each children first school experience is a positive foundation
			 for a lifetime of learning. A 4-1 student-to-teacher ratio allows teachers to offer
			 informed support, advice, encouragement, and affection. Each children day consists of 
			 one-on-one lessons with a teacher in addition to an art project and self-directed learning 
			 intended to build empowerment and independence. Each day also includes Circle Time 
			 (with singing and movement, story time, and show and tell), three outside play times, two snacks,
			 lunch, and a (much-needed) nap. Pre-Primary meets either three or five days a week from 8:30-3:30 
			 and is limited to 12 children.			
            </p>
             <h2>Primary
                 Ages 3-6
			 </h2>
			 <p>
             A deliberate balance of early learners and kindergarten students, the Primary classroom is 
			 the ideal environment for developing the basics of reading, writing, and practical life 
			 skills, as well as such intangibles as confidence, independence, and teamwork. 
			 Students devote several hours each day to independent learning and one-on-one work with teachers. 
			 There is an optional daily art project and a group gathering with teacher-led lessons.
			 Throughout the day, teachers observe and record student progress, creating an ever evolving 
			 and highly individualized learning plan catered to each child’s intellectual progress 
			 and personal development.			 
             </p>
